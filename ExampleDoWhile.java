package perulangandowhile;

public class ExampleDoWhile {

	public static void main(String[] args) {
		int number = 0;
		do {
			System.out.println("Angka lebih besar dari 10 :");
			
		}while (number > 10);
		System.out.println("pernyataan ini berasal dari luar while loop");
	
	}

}
